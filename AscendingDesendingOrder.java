class AscendingDesendingOrder
 {
	public static void main(String[] args)
	{
		System.out.println("Ascending order");
		for(int i=1; i<=10; i++)
		{
			System.out.println(i);
		}
		
		System.out.println("Decending order");
		for(int j=10; j>=1; j--)
		{
			System.out.println(j);
		}
	}
}
 

	